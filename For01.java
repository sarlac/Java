//for loop and while loop
//print the number: 1-10

public class For01
{
public static void main(String[] args)
{
for(int x=1; x<=10; x++)
{
System.out.println("Number : " + x);
}

System.out.println("\n****While loop Started******");

int num=1;
while(num<11)
{
System.out.println("number : " + num);
num++;
}
}
}